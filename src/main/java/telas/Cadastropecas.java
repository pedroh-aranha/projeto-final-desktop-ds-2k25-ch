    public void OcuparTabela() {
        model = (DefaultTableModel) tabela.getModel();
  
         model.setRowCount(0);
   
    PecasDAO dao = new PecasDAO();
    List<PecasBean> lista = dao.listarpecas();
   
    for (PecasBean t : lista) {
        model.addRow(new Object[]{
            t.getId(),
            t.getCod(),
            t.getNome(),
            t.getCod_orgnl()
        });
    }
}

